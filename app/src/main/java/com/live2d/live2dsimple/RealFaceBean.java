package com.live2d.live2dsimple;

public class RealFaceBean {
    private float angle_x;
    private float angle_y;
    private float angle_z;
    private float leftEyeOpenProbability;
    private float rightEyeOpenProbability;
    private float mouthHeight;

    public float getAngle_x() {
        return angle_x;
    }

    public void setAngle_x(float angle_x) {
        this.angle_x = angle_x;
    }

    public float getAngle_y() {
        return angle_y;
    }

    public void setAngle_y(float angle_y) {
        this.angle_y = angle_y;
    }

    public float getAngle_z() {
        return angle_z;
    }

    public void setAngle_z(float angle_z) {
        this.angle_z = angle_z;
    }

    public float getLeftEyeOpenProbability() {
        return leftEyeOpenProbability;
    }

    public void setLeftEyeOpenProbability(float leftEyeOpenProbability) {
        this.leftEyeOpenProbability = leftEyeOpenProbability;
    }

    public float getRightEyeOpenProbability() {
        return rightEyeOpenProbability;
    }

    public void setRightEyeOpenProbability(float rightEyeOpenProbability) {
        this.rightEyeOpenProbability = rightEyeOpenProbability;
    }

    public float getMouthHeight() {
        return mouthHeight;
    }

    public void setMouthHeight(float mouthHeight) {
        this.mouthHeight = mouthHeight;
    }
}
